package array;

public class Array1 {
    public static void main(String[] args) {
        int score1 = 80;
        int score2 = 70;
        int score3 = 60;
        int score4 = 20;

        System.out.println("학생1 점수: " + score1);
        System.out.println("학생2 점수: " + score2);
        System.out.println("학생3 점수: " + score3);
        System.out.println("학생4 점수: " + score4);

        // 배열의 필요성: 학생 수가 증가하면 코딩의 양이 비례하여 증가 -> 반복문 사용도 안됨

    }
}
